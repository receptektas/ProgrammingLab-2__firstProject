
public class DusmanDavranis {
	
	private int birim;
	
	private boolean atlar;
	
	public DusmanDavranis() {
		
	}

	public DusmanDavranis(int birim, boolean atlar) {
		super();
		this.birim = birim;
		this.atlar = atlar;
	}

	public int getBirim() {
		return birim;
	}

	public void setBirim(int birim) {
		this.birim = birim;
	}

	public boolean isAtlar() {
		return atlar;
	}

	public void setAtlar(boolean atlar) {
		this.atlar = atlar;
	}
	
	
}
