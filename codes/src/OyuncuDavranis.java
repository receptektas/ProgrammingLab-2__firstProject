
public class OyuncuDavranis {
	
	private int birim;

	private int puanAzman;

	private int puanGargamel;

	public OyuncuDavranis() {
		
	}

	public OyuncuDavranis(int birim, int puanAzman, int puanGargamel) {
		super();
		this.birim = birim;
		this.puanAzman = puanAzman;
		this.puanGargamel = puanGargamel;
	}

	public int getBirim() {
		return birim;
	}

	public void setBirim(int birim) {
		this.birim = birim;
	}

	public int getPuanAzman() {
		return puanAzman;
	}

	public void setPuanAzman(int puanAzman) {
		this.puanAzman = puanAzman;
	}

	public int getPuanGargamel() {
		return puanGargamel;
	}

	public void setPuanGargamel(int puanGargamel) {
		this.puanGargamel = puanGargamel;
	}
	
	
	
	
}
